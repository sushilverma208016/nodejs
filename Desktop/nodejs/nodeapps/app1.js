// JavaScript
var i=10;
console.log ( typeof i ); // type of data in i // number

i="10";
console.log ( typeof i ); // type of data in i // string

i= true;
console.log ( typeof i ); // type of data in i // boolean

i= function () {}
console.log ( typeof i ); // type of data in i // function

i= {}; // new Object();
console.log ( typeof i ); // type of data in i // object